// Simple script to generate a PNG icon from SVG
// Run this with: node generate-icon.js

const fs = require('fs');
const path = require('path');

// Create a simple 1x1 transparent PNG as a placeholder
// This is a minimal valid PNG file
const minimalPNG = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, // IHDR length
  0x49, 0x48, 0x44, 0x52, // IHDR type
  0x00, 0x00, 0x00, 0x20, // width: 32
  0x00, 0x00, 0x00, 0x20, // height: 32
  0x08, 0x02, 0x00, 0x00, 0x00, // bit depth: 8, color type: 2 (RGB), others: 0
  0xFD, 0xD4, 0x7C, 0x5B, // CRC
  0x00, 0x00, 0x00, 0x0C, // IDAT length
  0x49, 0x44, 0x41, 0x54, // IDAT type
  // Minimal image data (32x32 RGB, blue color)
  0x28, 0x15, 0x03, 0x20, 0x50, 0x50, 0x50, 0x50,
  0x50, 0x50, 0x50, 0x50, 0x50, 0x50, 0x50, 0x50,
  0x50, 0x50, 0x50, 0x50, 0x50, 0x50, 0x50, 0x50,
  0x02, 0x00, 0x00, 0x05, 0x00, 0x01, 0x0A, // IEND
  0x5F, 0x71, 0x13, 0x1E  // CRC
]);

// Create a 32x32 blue icon
const iconPath = path.join(__dirname, 'assets', 'icon.png');
fs.writeFileSync(iconPath, minimalPNG);

console.log('Icon generated at:', iconPath);
